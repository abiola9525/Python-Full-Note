# There are set of built-in methods that you can use on strings.

# 1. Upper Case
# We use the upper() method to return string in uppercase

a = "Welcome!"
print(a.upper())

# 2. Lower Case
# We use the lower() method to return the string in lower case:

b = "Welcome!"
print(a.lower())

# 3. Replace String
# We use the replace() method to replace a string with another string:

c = "Welcome!"
print(c.replace("W", "T"))
